// Comparision Operator

// Equal ==
// Not Equal !=
// Greater than >
// Greater than equals >=
// Less than <
// Less than equals <=
// Strict Equal ===
// Strict Not Equal !==

var a=100, b=200;

console.log("a == b: ", a==b)
console.log("a != b: ", a!=b)
console.log("a > b: ", a>b)
console.log("a < b: ", a<b)
console.log("a >= b: ", a>=b)
console.log("a <= b: ", a<=b)

//Strict equal
var x=10, y="10";
console.log("x == y: ", x==y)
console.log("x === y: ", x===y)

//Strict not equal
var x=10, y="10";
console.log("x != y: ", x!=y)
console.log("x !== y: ", x!==y)
